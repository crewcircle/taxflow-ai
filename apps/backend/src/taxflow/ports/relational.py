"""Port Protocols for relational data access (repository-per-aggregate).

Replaces the dual Supabase-PostgREST + raw-psycopg2 access with a single
port. Concrete repositories live in ``taxflow.adapters.db.repositories`` and use
the psycopg2 connection pool (``taxflow.db.get_pg_conn``). Every client-scoped
query carries an explicit ``WHERE client_id = %s`` predicate (RLS today keys on
``auth.role() = 'service_role'`` and provides no per-client isolation, so scoping
must live in the query).

Method signatures below are the contract the concrete ``Repositories`` facade
implements; each returns plain dicts/lists matching the shapes routers use today.
"""

from __future__ import annotations

from typing import Any, Protocol, runtime_checkable


@runtime_checkable
class ClientsRepo(Protocol):
    def get_by_email(self, email: str) -> dict | None: ...
    def get_by_id(self, client_id: str) -> dict | None: ...
    def create(self, row: dict) -> dict: ...
    def update(self, client_id: str, fields: dict) -> dict | None: ...
    def set_subscription_by_stripe_subscription_id(self, subscription_id: str, fields: dict) -> None: ...
    def set_subscription_by_customer_id(self, customer_id: str, fields: dict) -> None: ...
    def activate_from_checkout(self, client_id: str, fields: dict) -> None: ...
    def find_demo_emails(self, persona: str | None = None) -> list[str]: ...
    def email_exists(self, email: str) -> bool: ...
    def get_voice_sample(self, client_id: str) -> str | None: ...


@runtime_checkable
class UsersRepo(Protocol):
    """Individual logins within a firm (migration 043). ``id`` is the Supabase
    Auth user id (JWT ``sub``) — the stable identity key auth resolves by."""

    def get_by_id(self, user_id: str) -> dict | None: ...
    def get_by_client_and_email(self, client_id: str, email: str) -> dict | None: ...
    def create(
        self,
        user_id: str,
        client_id: str,
        email: str,
        role: str = "owner",
        display_name: str | None = None,
        invited_by: str | None = None,
        status: str = "active",
    ) -> dict: ...
    def list_for_client(self, client_id: str) -> list[dict]: ...
    def update_role(self, client_id: str, user_id: str, role: str) -> dict | None: ...
    def set_status(self, client_id: str, user_id: str, status: str) -> dict | None: ...
    def count_active_owners(self, client_id: str) -> int: ...


