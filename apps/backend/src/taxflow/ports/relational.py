@runtime_checkable
class RelationalDataPort(Protocol):
    """Facade exposing one repository per aggregate."""

    clients: ClientsRepo
    users: UsersRepo
    trials: TrialsRepo
    queries: QueriesRepo
    query_feedback: QueryFeedbackRepo
    annotations: AnnotationsRepo
    documents: DocumentsRepo
    firm_clients: FirmClientsRepo
    engagements: EngagementsRepo
    engagement_backfill: EngagementBackfillRepo
    query_sessions: QuerySessionsRepo
    document_templates: DocumentTemplatesRepo
    firm_knowledge: FirmKnowledgeRepo
    knowledge_suggestions: KnowledgeSuggestionsRepo
    engagement_context: EngagementContextRepo
    regulatory_alerts: RegulatoryAlertsRepo
    contact: ContactRepo
    rate_limit: RateLimitRepo
    query_cache: QueryCacheRepo
    knowledge_ingest: KnowledgeIngestRepo
    demo_reset: DemoResetRepo
    health: HealthRepo
    re_research_jobs: ReResearchJobsRepo
    notifications: NotificationsRepo
    ops_notifications: OpsNotificationsRepo
    production_snapshots: ProductionSnapshotsRepo


# Convenience alias for adapters that accept extra kwargs dicts.
